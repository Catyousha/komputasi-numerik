from numpy import array, arange, zeros, ones, float

simple_matrix = array([[2.0, -1.0],
                       [-1.0, 3.0]])
print(simple_matrix)

simple_arange = arange(2.0, 10.0, 2.0)
print(simple_arange)

simple_zeros = zeros((4))
print(simple_zeros)

simple_ones = ones((3,3), float)
print(simple_ones)